package project.workresult;

public class WorkResult {
	public int resultNo;
	public int workResult; //총합
	public String workData;
	public String resultDate ;
	public String memberID ;
	public int getResultNo() {
		return resultNo;
	}
	public void setResultNo(int resultNo) {
		this.resultNo = resultNo;
	}
	public int getWorkResult() {
		return workResult;
	}
	public void setWorkResult(int workResult) {
		this.workResult = workResult;
	}
	public String getWorkData() {
		return workData;
	}
	public void setWorkData(String workData) {
		this.workData = workData;
	}
	public String getResultDate() {
		return resultDate;
	}
	public void setResultDate(String resultDate) {
		this.resultDate = resultDate;
	}
	public String getMemberID() {
		return memberID;
	}
	public void setMemberID(String memberID) {
		this.memberID = memberID;
	}
	
	
	
	
	
}
