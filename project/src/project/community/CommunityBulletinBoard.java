package project.community;

public class CommunityBulletinBoard { 
	public int communityBulletinBoardNo;
	public String communityBulletinBoardName;
	public String communityKindOfbulletinBoard;
	public int openingDate;
	public int closingDate;
	public String creator;
	public String closer;
	
	public int getCommunityBulletinBoardNo() {
		return communityBulletinBoardNo;
	}
	public void setCommunityBulletinBoardNo(int communityBulletinBoardNo) {
		this.communityBulletinBoardNo = communityBulletinBoardNo;
	}
	public String getCommunityBulletinBoardName() {
		return communityBulletinBoardName;
	}
	public void setCommunityBulletinBoardName(String communityBulletinBoardName) {
		this.communityBulletinBoardName = communityBulletinBoardName;
	}
	public String getCommunityKindOfbulletinBoard() {
		return communityKindOfbulletinBoard;
	}
	public void setCommunityKindOfbulletinBoard(String communityKindOfbulletinBoard) {
		this.communityKindOfbulletinBoard = communityKindOfbulletinBoard;
	}
	public int getOpeningDate() {
		return openingDate;
	}
	public void setOpeningDate(int openingDate) {
		this.openingDate = openingDate;
	}
	public int getClosingDate() {
		return closingDate;
	}
	public void setClosingDate(int closingDate) {
		this.closingDate = closingDate;
	}
	public String getCreator() {
		return creator;
	}
	public void setCreator(String creator) {
		this.creator = creator;
	}
	public String getCloser() {
		return closer;
	}
	public void setCloser(String closer) {
		this.closer = closer;
	}
	
}